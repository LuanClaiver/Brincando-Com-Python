#EX 4 - Preco da passagem
distanciaPassageiroKm = int(input("Insira a distancia a ser percorrida em KM:"))

if distanciaPassageiroKm <= 200:
  precoPassagem = float(0.50 * distanciaPassageiroKm)
  print("Preco da passagem:", precoPassagem, "Reais")

elif distanciaPassageiroKm > 200:
  precoPassagem = float(0.45 * distanciaPassageiroKm)
  print("Preco da passagem:", precoPassagem, "Reais")
