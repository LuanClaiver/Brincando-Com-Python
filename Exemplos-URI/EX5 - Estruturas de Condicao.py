#EX5 - Estruturas de Condicao

print("Qual operacao voce deseja realizar?")
print("Operacoes disponiveis: (1)Adicao, (2)Subtracao, (3)Multiplicao, (4)Divisao")
operacao = int(input("Insira o numero da operacao:"))

numero1 = int(input("Insira um numero:"))
numero2 = int(input("Insira outro numero:"))

if operacao == 1:
  adicao = float(numero1 + numero2)
  print(numero1,"+",numero2,"=",adicao)

elif operacao == 2:
  subtracao = float(numero1 - numero2)
  print(numero1,"-",numero2,"=",subtracao)

elif operacao == 3:
  multiplicacao = float(numero1 * numero2)
  print(numero1,"x",numero2,"=",multiplicacao)

elif operacao == 4:
  divisao = float(numero1 / numero2)
  print(numero1,"/",numero2,"=",divisao)
